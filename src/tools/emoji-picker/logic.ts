// Check if an emoji supports skin tones (has a ZWJ or is a base emoji that accepts modifiers)
export function supportsSkinTone(emoji: string): boolean {
  // Test by appending a skin tone and checking if it renders as a single grapheme
  // Simpler approach: maintain a list of base code points that accept skin tones
  const cp = emoji.codePointAt(0);
  if (!cp) return false;
  // Unicode ranges for emoji that accept skin tone modifiers
  const skinToneRanges = [
    [0x261D, 0x261D], // index pointing up
    [0x26F9, 0x26F9], // person bouncing ball
    [0x270A, 0x270D], // fists, hands, writing hand
    [0x1F385, 0x1F385], // Santa
    [0x1F3C2, 0x1F3C4], // sports
    [0x1F3C7, 0x1F3C7],
    [0x1F3CA, 0x1F3CC],
    [0x1F442, 0x1F443], // ear, nose
    [0x1F446, 0x1F450], // pointing, hands
    [0x1F466, 0x1F478], // people
    [0x1F47C, 0x1F47C],
    [0x1F481, 0x1F483],
    [0x1F485, 0x1F487],
    [0x1F48F, 0x1F48F],
    [0x1F491, 0x1F491],
    [0x1F4AA, 0x1F4AA], // flexed biceps
    [0x1F574, 0x1F575],
    [0x1F57A, 0x1F57A],
    [0x1F590, 0x1F590],
    [0x1F595, 0x1F596],
    [0x1F645, 0x1F647],
    [0x1F64B, 0x1F64F],
    [0x1F6A3, 0x1F6A3],
    [0x1F6B4, 0x1F6B6],
    [0x1F6C0, 0x1F6C0],
    [0x1F6CC, 0x1F6CC],
    [0x1F90C, 0x1F90C],
    [0x1F90F, 0x1F90F],
    [0x1F918, 0x1F91F],
    [0x1F926, 0x1F926],
    [0x1F930, 0x1F939],
    [0x1F93D, 0x1F93E],
    [0x1F9B5, 0x1F9B6],
    [0x1F9B8, 0x1F9B9],
    [0x1F9BB, 0x1F9BB],
    [0x1F9CD, 0x1F9CF],
    [0x1F9D1, 0x1F9DD],
    [0x1FAC3, 0x1FAC5],
    [0x1FAF0, 0x1FAF6],
  ];
  return skinToneRanges.some(([lo, hi]) => cp >= lo && cp <= hi);
}

export function applySkinTone(emoji: string, modifier: string): string {
  if (!modifier || !supportsSkinTone(emoji)) return emoji;
  // Remove any existing skin tone modifier first
  const base = emoji.replace(
    new RegExp("[\u{1F3FB}-\u{1F3FF}]", "g"),
    ""
  );
  // Insert skin tone after the first code point
  const chars = Array.from(base);
  if (chars.length === 0) return emoji;
  return chars[0] + modifier + chars.slice(1).join("");
}

