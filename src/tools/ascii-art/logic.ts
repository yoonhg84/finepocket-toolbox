// ─── Font Registry ───────────────────────────────────────────────────────────

const FONTS: Record<string, Record<string, string[]>> = {
  Standard: FONT_STANDARD,
  Banner: FONT_BANNER,
  Big: FONT_BIG,
  Small: FONT_SMALL,
};

// ─── Public API ──────────────────────────────────────────────────────────────

export function getAvailableFonts(): string[] {
  return Object.keys(FONTS);
}

/**
 * Convert a text string into ASCII art using the specified font.
 * Unsupported characters are silently skipped.
 */
export function textToAsciiArt(text: string, fontName: string): string {
  const font = FONTS[fontName];
  if (!font) return `Unknown font: ${fontName}`;

  const upper = text.toUpperCase();
  const height = font[' ']?.length ?? 0;
  if (height === 0) return '';

  // Build rows
  const rows: string[] = Array.from({ length: height }, () => '');

  for (const ch of upper) {
    const glyph = font[ch];
    if (!glyph) continue; // skip unsupported chars

    for (let row = 0; row < height; row++) {
      rows[row] += glyph[row] ?? '';
    }
  }

  // Remove completely empty trailing lines
  while (rows.length > 0 && rows[rows.length - 1].trim() === '') {
    rows.pop();
  }

  return rows.join('\n');
}

/**
 * Get the maximum line width of the generated ASCII art.
 */
export function getAsciiArtWidth(art: string): number {
  if (!art) return 0;
  return Math.max(...art.split('\n').map((line) => line.length));
}
