export function searchEmojis(query: string, emojis: EmojiData[]): EmojiData[] {
  if (!query.trim()) return emojis;
  const lower = query.toLowerCase().trim();
  return emojis.filter(
    (e) =>
      e.name.toLowerCase().includes(lower) ||
      e.keywords.some((k) => k.includes(lower))
  );
}

export function getEmojisByCategory(category: EmojiCategory): EmojiData[] {
  return ALL_EMOJIS.filter((e) => e.category === category);
}

const RECENT_KEY = "emoji-picker-recent";
const MAX_RECENT = 32;

export function getRecentEmojis(): string[] {
  if (typeof window === "undefined") return [];
  try {
    const stored = localStorage.getItem(RECENT_KEY);
    return stored ? JSON.parse(stored) : [];
  } catch {
    return [];
  }
}

export function addRecentEmoji(emoji: string): string[] {
  const recent = getRecentEmojis().filter((e) => e !== emoji);
  recent.unshift(emoji);
  const trimmed = recent.slice(0, MAX_RECENT);
  try {
    localStorage.setItem(RECENT_KEY, JSON.stringify(trimmed));
  } catch {
    // localStorage full or unavailable
  }
  return trimmed;
}
